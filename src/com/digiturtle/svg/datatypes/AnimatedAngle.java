package com.digiturtle.svg.datatypes;

public class AnimatedAngle {
	
	private Angle baseValue, animatedValue;

	public Angle getBaseValue() {
		return baseValue;
	}

	public void setBaseValue(Angle baseValue) {
		this.baseValue = baseValue;
	}

	public Angle getAnimatedValue() {
		return animatedValue;
	}

	public void setAnimatedValue(Angle animatedValue) {
		this.animatedValue = animatedValue;
	}

}
